package OrientDBClient;

public class Movie {
	public String Title;
	public String Genre;
	public int Year;
	public int Length;
	
	public Movie()
	{
		
	}
	
	public Movie(String Title,String Genre,int Year,int Length)
	{
		this.Title = Title;
		this.Genre = Genre;
		this.Year = Year;
		this.Length = Length;
				
	}
	

}
